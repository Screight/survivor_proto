    public class AnimExplosiveEnemy : StateMachineBehaviour
    {
        Enemy m_enemy;
        // OnStateEnter is called when a transition starts and the state machine starts to evaluate this state
        override public void OnStateEnter(Animator animator, AnimatorStateInfo stateInfo, int layerIndex)
        {
            animator.transform.localScale = 2 * Vector2.one;
            if (m_enemy == null) { m_enemy = animator.GetComponent<Enemy>(); }
            m_enemy.SetVelocity(Vector2.zero, 0);
        }

